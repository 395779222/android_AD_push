package com.stargame.ad.entity;

public class Ad {
	private String imgUrl;
	private String appUrl;
	private String adId;
	public String getImgUrl() {
		return imgUrl;
	}
	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}
	public String getAppUrl() {
		return appUrl;
	}
	public void setAppUrl(String appUrl) {
		this.appUrl = appUrl;
	}
	public String getAdId() {
		return adId;
	}
	public void setAdId(String appId) {
		this.adId = appId;
	}
	
	
}
